package objects2;

public class Printer {
	private int println;
	private boolean println;
	private double println;
	private String println;
	
	public int getPrintln() {
		return println;
	}
	public void setPrintln(int println) {
		this.println = println;
	}
	public boolean isPrintln() {
		return println;
	}
	public void setPrintln(boolean println) {
		this.println = println;
	}
	public double getPrintln() {
		return println;
	}
	public void setPrintln(double println) {
		this.println = println;
	}
	public String getPrintln() {
		return println;
	}
	public void setPrintln(String println) {
		this.println = println;
	}
	
	
}
